package druhypolrok.tyzden08;

public class KontrolaPrimitivnychDatovychTypovAString {

    byte b;
    short s;
    int x;
    long l;
    float f;
    double y;
    char ch;
    String ss;
    boolean bb;

    @Override
    public String toString() {
        return "KontrolaPrimitivnychDatovychTypovAString{" +
                "b=" + b +
                ", s=" + s +
                ", x=" + x +
                ", l=" + l +
                ", f=" + f +
                ", y=" + y +
                ", ch=" + ch +
                ", ss='" + ss + '\'' +
                ", bb=" + bb +
                '}';
    }
}
